    private void consultas(String consulta) {
        eleccion = consulta;
        ConsultasBaseDatos actualizacion = new ConsultasBaseDatos();

        String ruta = "AD52425/EjercicioConsulta03" + consulta + ".xq";
        System.out.println(ruta);
        String actualizar = actualizacion.leerArchivo(ruta);
        String resultado = actualizacion.AbrirBaseDatos(actualizar);
        monstrarActualizacion(resultado);
    }

    private void monstrarActualizacion(String resultado) {
        Vector<String> tableHeader = new Vector<String>();
        Vector tableData = new Vector();
        String resultadoLimpio = resultado.replace("\n", " ");
        String[] campos = resultadoLimpio.split(" ");
        if (eleccion == "A") {
            tableHeader.add("Mátricula");
            tableHeader.add("Kilometros");

            String marca = "";
            String modelo = "";

            for (int i = 0; i < campos.length; i++) {
                Vector<String> onRow = new Vector<String>();
                if (i % 2 == 0) {
                    marca = campos[i];
                } else {
                    modelo = campos[i];

                    onRow.add(marca);
                    onRow.add(modelo);
                    tableData.add(onRow);
                }
            }
        }
        if (eleccion == "B") {
            jTextAreaXML.setText(resultado);
            System.out.println(resultado);

        }
        if (eleccion == "C") {
            jTextAreaXML.setText(resultado);
            System.out.println(resultado);
        }

        jTable1.setModel(new DefaultTableModel(tableData, tableHeader));
    }


