    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        ConsultasBaseDatos actualizacion = new ConsultasBaseDatos();
        String rutaActualizacion = null;
        String rutaConsulta = null;
        boolean resultadoTextArea = false;

        if (A.isSelected()) {

            rutaActualizacion = "AD52425/Ejercicio03A.xq";
            rutaConsulta = "AD52425/EjercicioConsulta03A.xq";
        }
        if (B.isSelected()) {
            rutaActualizacion = "AD52425/Ejercicio03B.xq";
            rutaConsulta = "AD52425/EjercicioConsulta03B.xq";
            resultadoTextArea = true;
        }
        if (C.isSelected()) {
            rutaActualizacion = "AD52425/Ejercicio03C.xq";
            rutaConsulta = "AD52425/EjercicioConsulta03C.xq";
            resultadoTextArea = true;
        }
        String actualizar = actualizacion.leerArchivo(rutaActualizacion);
        String actualizarConsulta = actualizacion.leerArchivo(rutaConsulta);
        String resultado = actualizacion.Actualizar(actualizar, actualizarConsulta);
        if (resultadoTextArea) {
            jTextAreaXML.setText(resultado);
            System.out.println(resultado);
        } else {
            monstrarActualizacion(resultado);
        }

        System.out.println(resultado);

    }//GEN-LAST:event_jToggleButton1ActionPerformed

